SELECT INITCAP(LAST_NAME) AS "Name", LENGTH(LAST_NAME) AS "Length"
FROM HR.EMPLOYEES
WHERE UPPER(LAST_NAME) LIKE 'J%' OR UPPER(LAST_NAME) LIKE 'A%' OR UPPER(LAST_NAME) LIKE 'M%'
ORDER BY LAST_NAME;


SELECT INITCAP(LAST_NAME) AS "Name", LENGTH(LAST_NAME) AS "Length"
FROM HR.EMPLOYEES
WHERE UPPER(LAST_NAME) LIKE '&user_prompt%'
ORDER BY LAST_NAME;


SELECT INITCAP(LAST_NAME) AS "Name", LENGTH(LAST_NAME) AS "Length"
