ORDER BY LAST_NAME;

-- 6. Command: 
SELECT LAST_NAME, ROUND(MONTHS_BETWEEN(SYSDATE, HIRE_DATE)) AS "MONTHS_WORKED"
FROM HR.EMPLOYEES
ORDER BY MONTHS_WORKED;


-- 7. Command: 
SELECT LAST_NAME, LPAD(SALARY, 15, '$') AS "SALARY"
FROM HR.EMPLOYEES;


-- 8. Command: 
SELECT SUBSTR(LAST_NAME,1,8) || ' ' || LPAD('*', FLOOR(SALARY / 1000), '*') AS "EMPLOYEES_AND_THEIR_SALARIES"
FROM HR.EMPLOYEES
ORDER BY SALARY DESC;

